function serializeBirthData(profile: AstroProfile): string {
  const loc = profile.notes?.split('(')[0]?.trim() ?? '–¢–∏–∫—Å–∏';
  return `${profile.birthDate}, ${profile.birthTime ?? '12:00'}, ${loc}`;
}

