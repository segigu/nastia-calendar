  // –ù–∞—Ö–æ–¥–∏–º –ø–æ–∑–∏—Ü–∏—é –°–æ–ª–Ω—Ü–∞ (–∑–Ω–∞–∫ –∑–æ–¥–∏–∞–∫–∞) - —ç—Ç–æ –í–ê–ñ–ù–û –¥–ª—è –ø–æ–Ω–∏–º–∞–Ω–∏—è —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∞
  const sunPlacement = analysis.corePlacements.find(p => p.includes('–°–æ–ª–Ω—Ü–µ'));

  // –ë–µ—Ä—ë–º –æ—Å—Ç–∞–ª—å–Ω—ã–µ –ø–ª–∞–Ω–µ—Ç—ã (–±–µ–∑ –°–æ–ª–Ω—Ü–∞)
  const otherPlacements = analysis.corePlacements
    .filter(p => !p.includes('–°–æ–ª–Ω—Ü–µ'))
    .slice(0, 4);

  // –°–æ–ª–Ω—Ü–µ –í–°–ï–ì–î–ê –ø–µ—Ä–≤—ã–º, –ø–æ—Ç–æ–º –æ—Å—Ç–∞–ª—å–Ω—ã–µ
